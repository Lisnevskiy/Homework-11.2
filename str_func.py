def make_all_letters_uppercase(string: str):
    """
    Принимает на вход строку и возвращает ее со всеми заглавными буквами
    """
    return string.upper()


def capitalize_first_letter(string: str):
    """
    Принимает строку и делает заглавными первые буквы каждого слова.
    :param string: str
    :return: str
    """
    return string.title()
